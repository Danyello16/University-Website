<h2>Kurze Benutzeranleitung:</h2>
<br>
<p>Im folgenden wird Ihnen eine kurze Einführung in die Benutzung der Website dargelegt.</p>
<p>Der Benutzer der Website kann sich auf der Webseite mit seinen Benutzernamen und sein Passwort einloggen, mit der Bedingung, dass er einen Account besitzt oder das er selbst Admin der Webseite ist.
Neben der Login-funktion, oben rechts, ist auch ein News- bzw. Aboutbutton eingerichtet.</p>
<p><h4>News</h4> Im Newsbereich werden Artikel über die wichtigsten Ereignisse aus der FH angezeigt.</p>
<h4>About</h4> Im Aboutbereich werden Sie eine kurze Beschreibung über die, die das Webseite erstellt haben, und der Fachhochschule Technikum Wien.

<p><strong>Ein eingeloggter User kann auf weitere Funktionen greifen:</strong></p>
Im Shopbereich können Sie unsere Produkte ansehen, und bestellen. Falls Ihnen ein Produkt gefällt, können Sie mit dem Kaufen-Button, das Produkt in den Warenkorb legen.


