Die Website „FHTW Shop&News“ präsentiert Neuigkeiten zur FH TechnikumWien (können von Administratoren selbst erstellt und mit Bildern ergänzt
werden, welche hochgeladen werden können) und bietet die Möglichkeit
Merchandising-Produkte (sind fix vorgegeben in Form einer JSON-Datei) zu
bestellen. Weiters gibt es einen Info-Bereich mit einer kurzen Einführung in das
Gebäude der FH Technikum-Wien in Form eines selbst erstellten Videos. Ein
Impressum, ein Linkbereich sowie ein Bereich für Werbebanner runden die
Inhalte der Website ab.
Es gibt 3 verschiedene Arten von Usern (Admin, registrierte User, anonym),
welche die Website unterschiedlich präsentiert bekommen. Da der Umgang mit
Datenbanken vorerst noch nicht bekannt ist, werden die User samt Usertyp und
Zugangsdaten in einer (statischen) XML-Datei verwaltet, die serverseitig
abgelegt ist.
Die Website ist responsive aufgebaut und präsentiert sich auf Smartphones,
Tablets und Desktops unterschiedlich. Usability, Accessibility und
Suchmaschinenoptimierung sind wichtige Kriterien.    
        

